//Question 27: Alien Colors #3: Convert your if-else chain to handle three colors:
// green, yellow, red.
// This exercise introduces you to handling multiple specific conditions in an if-else chain.
var alien_colour = "red";
if (alien_colour == "green") {
    console.log("you won!");
}
else if (alien_colour == "yellow") {
    console.log("you are in danger.");
}
else if (alien_colour = "red") {
    console.log("Try again");
}
else {
    console.log("choose correct option.");
}
